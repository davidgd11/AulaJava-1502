package br.com.fiap;

public class Constante {

	public static void main(String[] args) {
		//constante em Java
		//convenção: toda maiúscula e com underscore (espaço)
		
		final double PI = 3.1416;
		final float GRAVIDADE_TERRESTRE = 9.8f;
		
		System.out.println("Valor do PI é: " + PI);
		System.out.println("Valor da gravidade terrestre é de: " + GRAVIDADE_TERRESTRE);
		
	}
}
